import abc


class UserReadableException(Exception, metaclass=abc.ABCMeta):
    """
    An exception whose user_readable_message property can be safely displayed in the (web) app without confusing the user.
    """

    def __init__(self, user_readable_message: str):
        super().__init__("An user-readable exception has been raised with the following message: {}".format(user_readable_message))

        # The user readable message might also be an flask.Markup object (flask.Markup is a subclass of str).
        self.user_readable_message: str = user_readable_message
