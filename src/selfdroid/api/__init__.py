import flask
from selfdroid.Constants import Constants


URL_PREFIX = "/api"

api_blueprint = flask.Blueprint("api_blueprint", __name__, url_prefix=URL_PREFIX)


from selfdroid.api.v1 import api_v1_blueprint
api_blueprint.register_blueprint(api_v1_blueprint)


@api_blueprint.route("/", methods=["GET"])
def fl_api_base():
    # This public endpoint is used for API version discovery
    return flask.jsonify({
        "is_selfdroid_api": True,
        "supported_api_versions": Constants.SUPPORTED_API_VERSIONS
    })
